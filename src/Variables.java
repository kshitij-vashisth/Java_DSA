public class Variables {
    public static void main(String[] args) {
        int a; // declaration
        a = 23; // initialization
        String name = "Kshitij";
        System.out.println(a); // if we use print instead of println, output will be 23Kshitij
        System.out.println(name);
        System.out.print("6"+"4");

        int x = 23;
        int y = 45;
        int sum = x + y;
        System.out.println("sum of "+x+" & "+y+" is "+sum+".");
        System.out.println("sum of "+x+" & "+y+" is "+x+y+".");
        System.out.println("sum of "+x+" & "+y+" is "+(x+y)+".");
    }
}
